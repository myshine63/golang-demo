package main

import "fmt"

func main() {
	// 位运算，会将数字转成二进制，然后从个低位到高位进行运算
	// << 左移运算符，相当于乘以2的n次方，右移相反
	fmt.Printf("32<<2=%d\n", 32<<2) // 32*4=128
	fmt.Printf("32>>2=%d\n", 32>>2) // 32/4=8
	fmt.Printf("32<<4=%d\n", 32<<4) // 32*16=512
	fmt.Printf("32>>4=%d\n", 32>>4) // 32/16=2
	fmt.Printf("32<<6=%d\n", 32<<5) // 32*64=2048
	fmt.Printf("32>>6=%d\n", 32>>5) // 值为1
	fmt.Printf("32>>6=%d\n", 32>>6) // 左移超过了，值为0
}
